<?php

namespace Drupal\tbwa_newsletter\Form;

use Drupal\Core\Entity\ContentEntityConfirmFormBase;
use Drupal\Core\Form\FormStateInterface;
use Drupal\Core\Url;

/**
 * Provides a form for deleting a tbwa_newsletter entity.
 *
 * @ingroup tbwa_newsletter
 */
class NewsletterDeleteForm extends ContentEntityConfirmFormBase {

  /**
   * {@inheritdoc}
   */
  public function getQuestion() {
    return $this->t('Are you sure you want to delete entity %name?', array('%name' => $this->entity->id()));
  }

  /**
   * {@inheritdoc}
   *
   * If the delete command is canceled, return to the contact list.
   */
  public function getCancelUrl() {
    return new Url('entity.tbwa_newsletter.collection');
  }

  /**
   * {@inheritdoc}
   */
  public function getConfirmText() {
    return $this->t('Delete');
  }

  /**
   * {@inheritdoc}
   *
   * Delete the entity and log the event. logger() replaces the watchdog.
   */
  public function submitForm(array &$form, FormStateInterface $form_state) {
    $entity = $this->getEntity();
    $entity->delete();

    $this->logger('tbwa_newsletter')->notice('@type: deleted %title.',
      array(
        '@type' => $this->entity->first_name(),
        '%title' => $this->entity->id(),
      ));
    $form_state->setRedirect('entity.tbwa_newsletter.collection');
  }

}
